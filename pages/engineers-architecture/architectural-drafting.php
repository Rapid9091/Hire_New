    <p class="text_content">Architectural drafting services come in various forms, each catering to specific project
        requirements. These include:</p><br>
    <p class="text_content"><strong> 1)2D Drafting: </strong>Traditional 2D drafting involves creating detailed floor
        plans, elevations, and sections. It provides a clear visual representation of the layout and design elements.
