        exterior aesthetics, architectural drawings set the tone for the entire project.</p><br>
    <p class="text_content"><strong> 2)Structural Drawings:</strong> Detailing load-bearing elements and structural
        systems, these drawings ensure the building's stability and safety.</p><br>
    <p class="text_content"><strong> 3)Electrical Drawings:</strong> These diagrams outline the placement of electrical
        outlets, lighting, and wiring, facilitating a seamless power distribution system.</p><br>
    <p class="text_content"><strong>4)Plumbing Drawings:</strong> Plumbing layouts depict water supply, drainage, and
        piping systems, enabling efficient resource management.</p><br>
    <p class="text_content"><strong> 5)Mechanical Drawings:</strong> Mechanical systems drawings encompass HVAC
        (Heating, Ventilation, and Air Conditioning) layouts, ensuring indoor comfort.</p><br>
</div>
