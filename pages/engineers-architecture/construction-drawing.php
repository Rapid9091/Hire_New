    <h2 class="content-heading">What is Construction Drawing</h2>
    <p class="text_content">A construction drawing isn't just lines on paper. It's like a picture of what an architect
        wants to build, the math an engineer does, and the skills of a builder. It's a miniature copy that shows all the
        details of a building, including how it looks and how things like electricity, water, and air systems work.
        These drawings are like a typical picture that everyone building the item can see.</p><br>
</div>
<div class="content_div">
